class Main {
  public static void main(String[] args) {

    // 8 datatypes

    //boolean - Stores true or false values
    boolean isOn = false;
    System.out.println(isOn);
    isOn = true;
    System.out.println(isOn);
    // char - Stores a single character/letter or ASCII values
    char someChar = 'A';
    System.out.println(someChar);
    // whole number datatypes

    // byte - Stores whole numbers from -128 to 127
    byte aByte = 100;
    System.out.println(aByte);

    byte bByte = 120;
    System.out.println(bByte);
    // short - Stores whole numbers from -32,768 to 32,767
    short aShort; //declaring a variable
    aShort = 345;
    System.out.println(aShort);

    //int -2,147,483,648 to 2,147,483,647
    int aInt = 3242324;
    System.out.println(aInt);

    // long 0 -9,223,372,036,854,775,808 to 9,223,372,036,854,775,807
    long aLong = 2147483648L;
    System.out.println(aLong);

    // decimal number datatypes
    //double - Sufficient for storing 15 decimal digits
    double aDouble = 450.045435;
    System.out.println(aDouble);

    //float - Sufficient for storing 6 - 7 decimal digits
    float aFloat = 450.045435f;
    System.out.println(aFloat);

    
    System.out.println("Hello world!");
  }
}